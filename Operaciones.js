// 1. Multiplicación (función clásica)
function multiplicaC(a, b) {
  return a * b;
}

// 2. Multiplicación (arrow function)
const multiplicarArrow = (a, b) => a * b;
console.log(multiplicarArrow(5, 6));

// 3. Elevar al cuadrado (función clásica)
function cuadrado(c) {
  return c * c;
}

// 4. Elevar al cuadrado (arrow function)
const cuadradoArrow = c => c * c;
console.log(cuadradoArrow(7));      
